_DEFAULT_CAPABILITIES: Optional[List[Capability]] = None


def _capabilities_from_schema() -> List[Capability]:
    """Build Capability objects from the external schema."""
    schema = load_capability_schema()
    caps: List[Capability] = []
    for entry in schema.get("capabilities", []):
        try:
            caps.append(Capability(
                id=entry["id"],
                name=entry.get("name", entry["id"]),
                type=entry.get("type", "model"),
                description=entry.get("description", ""),
                runtime=entry.get("runtime", "model"),
                target=entry.get("target", ""),
                priority=entry.get("priority", 50),
                enabled=entry.get("enabled", True),
                tags=entry.get("tags", []),
                metadata=entry.get("metadata", {}),
            ))
        except Exception:
            continue
    return caps


def load_capabilities() -> List[Capability]:
    """Load capabilities: runtime overrides if present, else schema."""
    global _DEFAULT_CAPABILITIES
    runtime_path = _runtime_registry_path()
    if runtime_path.exists():
        try:
            data = json.loads(runtime_path.read_text(encoding="utf-8"))
            return [Capability.model_validate(item) for item in data]
        except Exception:
            pass
    # Fallback to schema
    caps = _capabilities_from_schema()
    if caps:
        _DEFAULT_CAPABILITIES = caps
        return caps
    # Last resort: empty list
    return []


def ensure_registry() -> None:
    """Create runtime registry file when missing (from schema)."""
    caps = load_capabilities()
    if caps and not _runtime_registry_path().exists():
        save_capabilities(caps)


def list_capabilities(enabled_only: bool = False) -> List[dict]:
    """Return capability dictionaries for API responses.

    Includes schema/runtime capabilities, own builtin modules (builtin.*),
    and default skills (skill.*).  Builtin modules are listed before
    external-reference skills so that planners prefer owned implementations.
    """
    items = load_capabilities()
    if enabled_only:
        items = [item for item in items if item.enabled]
    result = [item.model_dump() for item in items]
    existing_ids = {str(x.get("id")) for x in result}
    # Own builtin modules are added before external-reference skills.
    for module in load_builtin_modules():
        cap = _builtin_module_to_capability(module)
        if cap.get("id") not in existing_ids:
            result.append(cap)
            existing_ids.add(str(cap.get("id")))
    # Append default skills that are not already present and not disabled
    for skill in load_default_skills():
        cap = _skill_to_capability(skill)
        if not cap.get("enabled", True):
            continue
        if cap.get("id") not in existing_ids:
            result.append(cap)
            existing_ids.add(str(cap.get("id")))
    return result


def get_capability(capability_id: str) -> Optional[Capability]:
    """Return one capability by id."""
    for item in load_capabilities():
        if item.id == capability_id:
            return item
    return None


def save_capabilities(items: List[Capability]) -> None:
    """Persist runtime capability registry."""
    path = _runtime_registry_path()
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        json.dumps([item.model_dump() for item in items], ensure_ascii=False, indent=2),
        encoding="utf-8",
    )


def upsert_capability(capability: Capability) -> Capability:
    """Insert or replace one capability."""
    items = load_capabilities()
    for index, item in enumerate(items):
        if item.id == capability.id:
            items[index] = capability
            save_capabilities(items)
            return capability
    items.append(capability)
    save_capabilities(items)
    return capability


# ---------------------------------------------------------------------------
# Schema-driven matching — no hardcoded keyword tables
# ---------------------------------------------------------------------------

def match_capabilities(
    query: str,
    intent: str | None = None,
    tags: list[str] | None = None,
    limit: int = 5,
) -> List[Capability]:
    """Match a query to enabled capabilities using schema text overlap scoring.

    Scoring is based on term overlap between the query and the combined
    descriptive text of each capability (id, name, type, description,
    tags, aliases).  No hardcoded Chinese keyword tables are used.

    Default skills (skill.*) are also included in matching.
    """
    tags = tags or []
    query_terms = _terms(query)
    if not query_terms:
        return []

    # Build the full candidate pool: schema/runtime + builtin modules + default skills
    all_caps: list[Capability] = list(load_capabilities())

    existing_ids = {c.id for c in all_caps}

    # Add own builtin modules before external-reference skills
    for module in load_builtin_modules():
        cap_dict = _builtin_module_to_capability(module)
        if cap_dict.get("id") not in existing_ids:
            try:
                all_caps.append(Capability(**cap_dict))
                existing_ids.add(str(cap_dict.get("id")))
            except Exception:
                continue

    # Add default skills as Capability objects
    for skill in load_default_skills():
        cap_dict = _skill_to_capability(skill)
        if cap_dict.get("id") not in existing_ids:
            try:
                all_caps.append(Capability(**cap_dict))
                existing_ids.add(str(cap_dict.get("id")))
            except Exception:
                continue

    # Load schema to get aliases for scoring
    schema = load_capability_schema()
    alias_map: Dict[str, set[str]] = {}
    for entry in schema.get("capabilities", []):
        cap_id = entry.get("id", "")
        entry_terms: set[str] = set()
        for item in entry.get("aliases", []) or []:
            entry_terms.update(_terms(str(item)))
        for item in entry.get("tags", []) or []:
            entry_terms.update(_terms(str(item)))
        alias_map[cap_id] = entry_terms

    # Also add skill aliases from skill metadata.original_tags
    for skill in load_default_skills():
        cap_id = f"skill.{skill.get('id', '')}"
        skill_terms: set[str] = set()
        for item in skill.get("tags", []) or []:
            skill_terms.update(_terms(str(item)))
        for item in skill.get("original_tags", []) or []:
            skill_terms.update(_terms(str(item)))
        if cap_id not in alias_map:
            alias_map[cap_id] = skill_terms
        else:
            alias_map[cap_id].update(skill_terms)

    # Also add builtin module aliases from merged_capabilities and skill_tags
    for module in load_builtin_modules():
        cap_id = module.get("id", "")
        mod_terms: set[str] = set()
        for item in module.get("merged_capabilities", []) or []:
            mod_terms.update(_terms(str(item)))
        for ref in module.get("source_references", []) or []:
            for tag in ref.get("tags", []) or []:
                mod_terms.update(_terms(str(tag)))
        if cap_id not in alias_map:
            alias_map[cap_id] = mod_terms
        else:
            alias_map[cap_id].update(mod_terms)

    scored: list[tuple[float, Capability]] = []
    for capability in all_caps:
        if not capability.enabled:
            continue
        # Build text from capability fields
        cap_text = " ".join([
            capability.id,
            capability.name,
            capability.type,
            capability.description,
            " ".join(capability.tags),
            capability.runtime,
            capability.target,
        ])
        cap_terms = _terms(cap_text)
        # Merge schema alias terms
        cap_terms.update(alias_map.get(capability.id, set()))

        # Compute overlap score
        overlap = query_terms.intersection(cap_terms)
        base_score = len(overlap) / max(1, min(len(query_terms), len(cap_terms)))

        # Bonus for explicit tag match
        tag_bonus = 0.0
        for tag in tags:
            if tag in capability.tags:
                tag_bonus += 0.1

        # Bonus for intent match
        intent_bonus = 0.0
        if intent:
            intent_terms = _terms(intent)
            if intent_terms.intersection(cap_terms):
                intent_bonus = 0.15

        # Small priority-based tiebreaker (0..1 range, max ~1.0)
        priority_bonus = capability.priority / 1000.0

        score = round(base_score + tag_bonus + intent_bonus + priority_bonus, 4)

        # Prefer owned builtin modules over external-reference skills.
        meta = capability.metadata or {}
        if meta.get("builtin"):
            score += 0.12

        # Lifecycle boosts: pinned, usage, rating
        if meta.get("pinned"):
            score += 0.08
        usage_count = int(meta.get("usage_count") or 0)
        if usage_count > 0:
            score += min(0.05, usage_count * 0.005)
        rating_val = meta.get("rating")
        try:
            if rating_val is not None:
                score += max(0.0, min(0.05, float(rating_val) / 100.0))
        except (TypeError, ValueError):
            pass
        score = round(score, 4)

        if score > 0:
            scored.append((score, capability))

    scored.sort(key=lambda item: item[0], reverse=True)
    return [item[1] for item in scored[:limit]]
