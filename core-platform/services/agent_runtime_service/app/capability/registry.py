def load_default_skills() -> list[dict[str, Any]]:
    """Load enabled default skills from skill_brain/default_skills.json."""
    path = _default_skills_path()
    if not path.exists():
        return []
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return []
    skills = payload.get("skills", [])
    return [x for x in skills if isinstance(x, dict) and x.get("enabled_by_default", True)]


def load_builtin_modules() -> list[dict[str, Any]]:
    """Load own builtin fusion modules from skill_brain/our_builtin_skill_modules.json."""
    path = _builtin_modules_path()
    if not path.exists():
        return []
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return []
    modules = payload.get("modules", [])
    return [x for x in modules if isinstance(x, dict)]


def _builtin_contracts_path() -> Path:
    """Path to the builtin execution contracts JSON."""
    return _core_platform_dir() / "data" / "skill_brain" / "builtin_execution_contracts.json"


def load_builtin_contracts() -> dict[str, dict[str, Any]]:
    """Load builtin execution contracts keyed by module id."""
    path = _builtin_contracts_path()
    if not path.exists():
        return {}
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return {}
    contracts = payload.get("contracts", [])
    if not isinstance(contracts, list):
        return {}
    return {
        str(item.get("id")): item
        for item in contracts
        if isinstance(item, dict) and item.get("id")
    }


def _builtin_module_to_capability(module: dict[str, Any]) -> dict[str, Any]:
    """Convert a builtin fusion module into a capability dict (id prefix builtin.*).

    Since C26-R5 the returned dict includes the full execution contract:
    execution_contract, execution_mode, allowed_tools, input_schema,
    output_schema, acceptance_tests, runtime_dependency_policy.
    """
    module_id = str(module.get("id") or "").strip()
    title = str(module.get("title") or module_id).strip()
    merged = [str(x) for x in module.get("merged_capabilities", []) or []]
    plan = [str(x) for x in module.get("implementation_plan", []) or []]
    contract = load_builtin_contracts().get(module_id, {})
    tags: list[str] = []
    for ref in module.get("source_references", []) or []:
        for tag in ref.get("tags", []) or []:
            if tag not in tags:
                tags.append(str(tag))
    description = "；".join(merged[:6])
    allowed_tools = contract.get("allowed_tools") or [
        "capability.match",
        "repo_memory.search",
        "model.generate",
    ]
    return {
        "id": module_id,
        "name": title,
        "type": "own_builtin_module",
        "description": description,
        "runtime": "builtin_fusion",
        "target": "capability.match",
        "priority": 80,
        "enabled": True,
        "tags": tags,
        "input_schema": contract.get("input_schema") or {},
        "metadata": {
            "source_type": "own_builtin_module",
            "builtin": True,
            "builtin_priority": 1.0,
            "implementation_status": contract.get("implementation_status", module.get("implementation_status", "planned")),
            "source_reference_count": module.get("source_reference_count", 0),
            "merged_capabilities": merged,
            "implementation_plan": plan,
            "skill_tags": tags,
            "tools": allowed_tools,
            "risk": "safe",
            "execution_contract": contract,
            "execution_mode": contract.get("execution_mode"),
            "allowed_tools": allowed_tools,
            "output_schema": contract.get("output_schema"),
            "acceptance_tests": contract.get("acceptance_tests", []),
            "runtime_dependency_policy": contract.get("runtime_dependency_policy", "owned_code_only"),
        },
    }


def _skill_to_capability(skill: dict[str, Any]) -> dict[str, Any]:
    """Convert a default skill dict into a capability dict (id prefix skill.*).

    Merges lifecycle state from skill_state.json so that disabled/pinned/usage
    information flows into the capability registry.
    """
    skill_id = str(skill.get("id") or "").strip()
    title = str(skill.get("title") or skill_id).strip()
    description = str(skill.get("description") or "").strip()
    tags = [str(x) for x in skill.get("tags", []) or []]
    tools = [str(x) for x in skill.get("tools", []) or []] or [
        "repo_memory.search", "capability.match", "model.generate"
    ]
    # Merge lifecycle state
    lstate = get_skill_state(skill_id)
    enabled = bool(lstate.get("enabled", skill.get("enabled_by_default", True)))
    pinned = bool(lstate.get("pinned", False))
    usage_count = int(lstate.get("usage_count") or 0)
    rating = lstate.get("rating")
    lifecycle = lstate.get("lifecycle", "enabled" if enabled else "disabled")
    return {
        "id": f"skill.{skill_id}",
        "name": title,
        "type": "default_skill",
        "description": description,
        "runtime": "skill_brain",
        "target": "capability.match",
        "priority": 40,
        "enabled": enabled,
        "tags": tags,
        "input_schema": {},
        "metadata": {
            "source_type": "default_skill",
            "source_path": skill.get("source_path"),
            "source_repo": skill.get("source_repo"),
            "html_url": skill.get("html_url"),
            "skill_tags": tags,
            "tools": tools,
            "risk": str(skill.get("risk") or "safe"),
            "language": skill.get("language"),
            "stars": skill.get("stars"),
            "original_tags": skill.get("original_tags", []),
            "buckets": skill.get("buckets", []),
            "lifecycle": lifecycle,
            "pinned": pinned,
            "usage_count": usage_count,
            "rating": rating,
        },
    }


